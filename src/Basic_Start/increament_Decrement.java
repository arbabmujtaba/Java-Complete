public class increament_Decrement {
    public static void main(String[] args) {

        // we can write java code in a single line all the code
        int a = 10;
        System.out.println(a);
        a *= 20;
        System.out.println(a);
        a++;// first the value is printed then it is incremented post Increament
        System.out.println(a);
        a--;// first the value is printed then it is decremented pre increament
        System.out.println(a);
        ++a;
        System.out.println(a);
        --a;
        System.out.println(a);
    }
}
